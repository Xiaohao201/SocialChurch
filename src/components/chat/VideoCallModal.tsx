import React, { useState, useEffect, useRef } from 'react';
import { Button } from '@/components/ui/button';
import { Dialog, DialogContent, DialogTitle, DialogDescription } from '@/components/ui/dialog';
import { VideoCallService, VideoCallStatus, defaultVideoCallConfig } from '@/lib/webrtc/videoCall';
import { useUserContext } from '@/context/AuthContext';
import { getUserAvatarUrl } from '@/lib/appwrite/api';

interface VideoCallModalProps {
  isOpen: boolean;
  onClose: () => void;
  targetUser: {
    id: string;
    name: string;
    avatar?: string;
  };
  mode: 'outgoing' | 'incoming';
  incomingOffer?: RTCSessionDescriptionInit | null;
  onIncomingCall?: (fromUserId: string, callerInfo: { userId: string; offer: RTCSessionDescriptionInit; callerName?: string; callerAvatar?: string }) => void;
}

const VideoCallModal: React.FC<VideoCallModalProps> = ({
  isOpen,
  onClose,
  targetUser,
  mode,
  incomingOffer,
  onIncomingCall
}) => {
  const { user } = useUserContext();
  const [callStatus, setCallStatus] = useState<VideoCallStatus>('idle');
  const [isMuted, setIsMuted] = useState(false);
  const [isVideoEnabled, setIsVideoEnabled] = useState(true);
  const [callDuration, setCallDuration] = useState(0);
  const [error, setError] = useState<string | null>(null);
  const [remoteVideoEnabled, setRemoteVideoEnabled] = useState(true);
  const [remoteAudioEnabled, setRemoteAudioEnabled] = useState(true);
  
  const videoServiceRef = useRef<VideoCallService | null>(null);
  const localVideoRef = useRef<HTMLVideoElement>(null);
  const remoteVideoRef = useRef<HTMLVideoElement>(null);
  const callTimerRef = useRef<NodeJS.Timeout | null>(null);
  const [isServiceInitialized, setIsServiceInitialized] = useState(false);

  // ÂàùÂßãÂåñËßÜÈ¢ëÈÄöËØùÊúçÂä°
  useEffect(() => {
    const initializeService = async () => {
      if (isOpen && user?.$id && !videoServiceRef.current) {
        try {
          console.log('üöÄ ÂàùÂßãÂåñËßÜÈ¢ëÈÄöËØùÊúçÂä°');
          console.log('üë§ ÂΩìÂâçÁî®Êà∑:', user);
          console.log('üéØ ÁõÆÊ†áÁî®Êà∑:', targetUser);
          console.log('üìã Ê®°Âºè:', mode);
          videoServiceRef.current = new VideoCallService(defaultVideoCallConfig);
          
          // ËÆæÁΩÆÂõûË∞ÉÂáΩÊï∞
          videoServiceRef.current.setCallbacks({
            onStatusChange: (status) => {
              console.log('üìπ ËßÜÈ¢ëÈÄöËØùÁä∂ÊÄÅÂèòÂåñ:', status);
              setCallStatus(status);
              if (status === 'connected') {
                startCallTimer();
              } else if (status === 'ended' || status === 'rejected' || status === 'failed') {
                stopCallTimer();
                setTimeout(() => {
                  onClose();
                }, 2000);
              }
            },
            onLocalStream: (stream) => {
              console.log('üìπ Êú¨Âú∞ËßÜÈ¢ëÊµÅÂèØÁî®');
              if (localVideoRef.current) {
                localVideoRef.current.srcObject = stream;
                localVideoRef.current.play().catch(error => {
                  console.error('Êí≠ÊîæÊú¨Âú∞ËßÜÈ¢ëÂ§±Ë¥•:', error);
                });
              }
            },
            onRemoteStream: (stream) => {
              console.log('üì∫ Êé•Êî∂Âà∞ËøúÁ®ãËßÜÈ¢ëÊµÅ');
              if (remoteVideoRef.current) {
                remoteVideoRef.current.srcObject = stream;
                remoteVideoRef.current.play().catch(error => {
                  console.error('Êí≠ÊîæËøúÁ®ãËßÜÈ¢ëÂ§±Ë¥•:', error);
                });
              }
            },
            onError: (error) => {
              console.error('‚ùå ËßÜÈ¢ëÈÄöËØùÈîôËØØ:', error);
              setError(error.message);
              setCallStatus('failed');
            },
            onIncomingCall: (fromUserId, callerInfo) => {
              console.log('üìπ Êî∂Âà∞ËßÜÈ¢ëÈÄöËØùÈÇÄËØ∑:', fromUserId, callerInfo);
              if (onIncomingCall) {
                onIncomingCall(fromUserId, callerInfo);
              }
            },
            onRemoteVideoToggle: (enabled) => {
              console.log('üìπ ÂØπÊñπËßÜÈ¢ëÁä∂ÊÄÅÂèòÂåñ:', enabled);
              setRemoteVideoEnabled(enabled);
            },
            onRemoteAudioToggle: (enabled) => {
              console.log('üé§ ÂØπÊñπÈü≥È¢ëÁä∂ÊÄÅÂèòÂåñ:', enabled);
              setRemoteAudioEnabled(enabled);
            }
          });

          // ÂàùÂßãÂåñÁî®Êà∑
          await videoServiceRef.current.initializeUser(user.$id);
          
          // ËÆæÁΩÆÂΩìÂâçÁî®Êà∑‰ø°ÊÅØ
          const userInfo = {
            name: user.name || 'Êú™Áü•Áî®Êà∑',
            avatar: getUserAvatarUrl(user.imageUrl)
          };
          
          console.log('üìù ËÆæÁΩÆÂΩìÂâçÁî®Êà∑‰ø°ÊÅØ:', userInfo);
          videoServiceRef.current.setCurrentUserInfo(userInfo);
          
          setIsServiceInitialized(true);
          console.log('‚úÖ ËßÜÈ¢ëÈÄöËØùÊúçÂä°ÂàùÂßãÂåñÂÆåÊàê');
          console.log('üîß ÊúçÂä°Áä∂ÊÄÅ:', {
            isServiceInitialized: true,
            targetUserId: targetUser.id,
            mode: mode
          });

        } catch (error) {
          console.error('‚ùå ÂàùÂßãÂåñËßÜÈ¢ëÈÄöËØùÊúçÂä°Â§±Ë¥•:', error);
          setError('ÂàùÂßãÂåñËßÜÈ¢ëÈÄöËØùÊúçÂä°Â§±Ë¥•');
        }
      }
    };

    initializeService();

    return () => {
      if (videoServiceRef.current) {
        videoServiceRef.current.destroy();
        videoServiceRef.current = null;
        setIsServiceInitialized(false);
      }
      stopCallTimer();
    };
  }, [isOpen, user?.$id, onClose]);

  // Â§ÑÁêÜ‰º†ÂÖ•ÁöÑ offerÔºàÊù•ÁîµÔºâ
  useEffect(() => {
    if (incomingOffer && mode === 'incoming' && isServiceInitialized && videoServiceRef.current) {
      console.log('üìπ Â§ÑÁêÜËßÜÈ¢ëÈÄöËØù offer');
      setCallStatus('ringing');
    }
  }, [incomingOffer, mode, isServiceInitialized]);

  // ÂºÄÂßãÈÄöËØùËÆ°Êó∂Âô®
  const startCallTimer = () => {
    if (callTimerRef.current) {
      clearInterval(callTimerRef.current);
    }
    callTimerRef.current = setInterval(() => {
      setCallDuration(prev => prev + 1);
    }, 1000);
  };

  // ÂÅúÊ≠¢ÈÄöËØùËÆ°Êó∂Âô®
  const stopCallTimer = () => {
    if (callTimerRef.current) {
      clearInterval(callTimerRef.current);
      callTimerRef.current = null;
    }
  };

  // Ê†ºÂºèÂåñÈÄöËØùÊó∂Èïø
  const formatDuration = (seconds: number): string => {
    const mins = Math.floor(seconds / 60);
    const secs = seconds % 60;
    return `${mins.toString().padStart(2, '0')}:${secs.toString().padStart(2, '0')}`;
  };

  // ÂèëËµ∑ÈÄöËØù
  const handleStartCall = async () => {
    if (!videoServiceRef.current || !isServiceInitialized) {
      setError('ËßÜÈ¢ëÈÄöËØùÊúçÂä°Êú™ÂàùÂßãÂåñ');
      return;
    }

    try {
      await videoServiceRef.current.initiateCall(targetUser.id);
    } catch (error: any) {
      console.error('ÂèëËµ∑ËßÜÈ¢ëÈÄöËØùÂ§±Ë¥•:', error);
      setError(error.message || 'ÂèëËµ∑ËßÜÈ¢ëÈÄöËØùÂ§±Ë¥•');
    }
  };

  // Êé•Âê¨ÈÄöËØù
  const handleAnswerCall = async () => {
    if (!videoServiceRef.current || !isServiceInitialized || !incomingOffer) {
      setError('Êó†Ê≥ïÊé•Âê¨ËßÜÈ¢ëÈÄöËØù');
      return;
    }

    try {
      await videoServiceRef.current.answerCall(targetUser.id, incomingOffer);
    } catch (error: any) {
      console.error('Êé•Âê¨ËßÜÈ¢ëÈÄöËØùÂ§±Ë¥•:', error);
      setError(error.message || 'Êé•Âê¨ËßÜÈ¢ëÈÄöËØùÂ§±Ë¥•');
    }
  };

  // ÊãíÁªùÈÄöËØù
  const handleRejectCall = async () => {
    if (!videoServiceRef.current || !isServiceInitialized) {
      return;
    }

    try {
      await videoServiceRef.current.rejectCall(targetUser.id);
    } catch (error: any) {
      console.error('ÊãíÁªùËßÜÈ¢ëÈÄöËØùÂ§±Ë¥•:', error);
    }
    onClose();
  };

  // ÁªìÊùüÈÄöËØù
  const handleEndCall = async () => {
    if (!videoServiceRef.current) {
      return;
    }

    try {
      await videoServiceRef.current.endCall();
    } catch (error: any) {
      console.error('ÁªìÊùüËßÜÈ¢ëÈÄöËØùÂ§±Ë¥•:', error);
    }
  };

  // ÂàáÊç¢ÈùôÈü≥
  const handleToggleMute = () => {
    if (videoServiceRef.current) {
      const muted = videoServiceRef.current.toggleAudio();
      setIsMuted(muted);
    }
  };

  // ÂàáÊç¢ËßÜÈ¢ë
  const handleToggleVideo = () => {
    if (videoServiceRef.current) {
      const enabled = videoServiceRef.current.toggleVideo();
      setIsVideoEnabled(enabled);
    }
  };

  // Ëé∑ÂèñÁä∂ÊÄÅÊñáÊú¨
  const getStatusText = () => {
    switch (callStatus) {
      case 'calling':
        return 'Ê≠£Âú®ÂëºÂè´...';
      case 'ringing':
        return mode === 'incoming' ? 'ËßÜÈ¢ëÈÄöËØùÈÇÄËØ∑' : 'Á≠âÂæÖÊé•Âê¨...';
      case 'connected':
        return `ËßÜÈ¢ëÈÄöËØù‰∏≠ ${formatDuration(callDuration)}`;
      case 'ended':
        return 'ÈÄöËØùÂ∑≤ÁªìÊùü';
      case 'rejected':
        return 'ÈÄöËØùË¢´ÊãíÁªù';
      case 'failed':
        return 'ÈÄöËØùÂ§±Ë¥•';
      default:
        return 'ÂáÜÂ§á‰∏≠...';
    }
  };

  // Ëé∑ÂèñÁä∂ÊÄÅÈ¢úËâ≤
  const getStatusColor = () => {
    switch (callStatus) {
      case 'calling':
      case 'ringing':
        return 'text-yellow-500';
      case 'connected':
        return 'text-green-500';
      case 'ended':
        return 'text-gray-500';
      case 'rejected':
      case 'failed':
        return 'text-red-500';
      default:
        return 'text-light-2';
    }
  };

  // Ëá™Âä®ÂèëËµ∑ÈÄöËØùÔºàÂ§ñÊã®Ê®°ÂºèÔºâ
  useEffect(() => {
    if (mode === 'outgoing' && callStatus === 'idle' && isServiceInitialized) {
      handleStartCall();
    }
  }, [mode, callStatus, isServiceInitialized]);

  if (!isOpen) return null;

  return (
    <Dialog open={isOpen} onOpenChange={onClose}>
      <DialogContent className="sm:max-w-4xl max-w-full bg-dark-2 border-dark-4 p-0 overflow-hidden">
        <div className="relative w-full h-[80vh] bg-black">
          {/* ËøúÁ®ãËßÜÈ¢ë - ‰∏ªÁ™óÂè£ */}
          <div className="relative w-full h-full">
            {callStatus === 'connected' ? (
              <video 
                ref={remoteVideoRef} 
                autoPlay 
                playsInline
                className="w-full h-full object-cover"
                style={{ transform: 'scaleX(-1)' }} // ÈïúÂÉèÊòæÁ§∫ÔºåÊõ¥Ëá™ÁÑ∂
              />
            ) : (
              // ÈÄöËØùÂâçÊòæÁ§∫ÂØπÊñπÂ§¥ÂÉè
              <div className="w-full h-full flex items-center justify-center bg-gradient-to-br from-dark-3 to-dark-4">
                <div className="text-center">
                  {/* Áî®Êà∑Â§¥ÂÉè */}
                  <div className="relative mb-6">
                    {/* ËÉåÊôØÂÖâÁéØÊïàÊûú */}
                    {(callStatus === 'calling' || callStatus === 'ringing') && (
                      <div className="absolute inset-0 rounded-full bg-yellow-500/20 animate-ping scale-110" />
                    )}
                    
                    {/* ‰∏ªÂ§¥ÂÉè */}
                    <img
                      src={targetUser.avatar || '/assets/icons/profile-placeholder.svg'}
                      alt={targetUser.name}
                      className="relative w-32 h-32 rounded-full object-cover border-4 border-dark-4 shadow-2xl mx-auto"
                      onError={(e) => {
                        const target = e.target as HTMLImageElement;
                        if (target.src !== '/assets/icons/profile-placeholder.svg') {
                          if (targetUser.name && !target.src.includes('avatars/initials')) {
                            const initialsUrl = `https://fra.cloud.appwrite.io/v1/avatars/initials?name=${encodeURIComponent(targetUser.name)}&project=6846b9f900368f67ddb4`;
                            target.src = initialsUrl;
                          } else {
                            target.src = '/assets/icons/profile-placeholder.svg';
                          }
                        }
                      }}
                    />
                  </div>

                  {/* Áî®Êà∑ÂêçÂíåÁä∂ÊÄÅ */}
                  <DialogTitle className="text-2xl font-bold text-light-1 mb-2">
                    {targetUser.name}
                  </DialogTitle>
                  <DialogDescription className={`text-lg ${getStatusColor()}`}>
                    {getStatusText()}
                  </DialogDescription>
                  {error && (
                    <p className="text-red-500 text-sm mt-2">{error}</p>
                  )}
                </div>
              </div>
            )}

            {/* ËøúÁ®ãËßÜÈ¢ëÁä∂ÊÄÅË¶ÜÁõñÂ±Ç */}
            {callStatus === 'connected' && !remoteVideoEnabled && (
              <div className="absolute inset-0 bg-black/80 flex items-center justify-center">
                <div className="text-center text-white">
                  <div className="mb-4">
                    <img
                      src={targetUser.avatar || '/assets/icons/profile-placeholder.svg'}
                      alt={targetUser.name}
                      className="w-24 h-24 rounded-full object-cover mx-auto border-2 border-gray-400"
                    />
                  </div>
                  <p className="text-lg">{targetUser.name}</p>
                  <p className="text-sm text-gray-400">ÊëÑÂÉèÂ§¥Â∑≤ÂÖ≥Èó≠</p>
                </div>
              </div>
            )}

            {/* Êú¨Âú∞ËßÜÈ¢ë - Â∞èÁ™óÂè£ */}
            {callStatus === 'connected' && (
              <div className="absolute top-4 right-4 w-32 h-24 bg-black rounded-lg overflow-hidden border-2 border-gray-600 shadow-lg">
                {isVideoEnabled ? (
                  <video 
                    ref={localVideoRef} 
                    autoPlay 
                    playsInline
                    muted
                    className="w-full h-full object-cover"
                    style={{ transform: 'scaleX(-1)' }} // ÈïúÂÉèÊòæÁ§∫
                  />
                ) : (
                  <div className="w-full h-full bg-gray-800 flex items-center justify-center">
                    <span className="text-white text-xs">ÊëÑÂÉèÂ§¥ÂÖ≥Èó≠</span>
                  </div>
                )}
              </div>
            )}

            {/* ÊéßÂà∂ÊåâÈíÆ */}
            <div className="absolute bottom-6 left-1/2 transform -translate-x-1/2">
              <div className="flex items-center space-x-4 bg-black/50 backdrop-blur-sm rounded-full px-6 py-3">
                {/* ÈùôÈü≥ÊåâÈíÆ */}
                {(callStatus === 'connected' || callStatus === 'calling') && (
                  <Button
                    variant="ghost"
                    size="icon"
                    className={`w-12 h-12 rounded-full ${
                      isMuted ? 'bg-red-500 hover:bg-red-600' : 'bg-gray-600 hover:bg-gray-700'
                    }`}
                    onClick={handleToggleMute}
                  >
                    <span className="text-xl">{isMuted ? 'üîá' : 'üé§'}</span>
                  </Button>
                )}

                {/* ËßÜÈ¢ëÂàáÊç¢ÊåâÈíÆ */}
                {(callStatus === 'connected' || callStatus === 'calling') && (
                  <Button
                    variant="ghost"
                    size="icon"
                    className={`w-12 h-12 rounded-full ${
                      !isVideoEnabled ? 'bg-red-500 hover:bg-red-600' : 'bg-gray-600 hover:bg-gray-700'
                    }`}
                    onClick={handleToggleVideo}
                  >
                    <span className="text-xl">{isVideoEnabled ? 'üìπ' : 'üì∑'}</span>
                  </Button>
                )}

                {/* ‰∏ªË¶ÅÊìç‰ΩúÊåâÈíÆ */}
                {callStatus === 'ringing' && mode === 'incoming' ? (
                  <>
                    {/* Êé•Âê¨ÊåâÈíÆ */}
                    <Button
                      className="w-12 h-12 rounded-full bg-green-500 hover:bg-green-600"
                      onClick={handleAnswerCall}
                    >
                      <span className="text-xl">üìπ</span>
                    </Button>
                    {/* ÊãíÁªùÊåâÈíÆ */}
                    <Button
                      className="w-12 h-12 rounded-full bg-red-500 hover:bg-red-600"
                      onClick={handleRejectCall}
                    >
                      <span className="text-xl">‚ùå</span>
                    </Button>
                  </>
                ) : (
                  /* ÁªìÊùüÈÄöËØùÊåâÈíÆ */
                  <Button
                    className="w-12 h-12 rounded-full bg-red-500 hover:bg-red-600"
                    onClick={handleEndCall}
                    disabled={callStatus === 'idle' || !isServiceInitialized}
                  >
                    <span className="text-xl">üìû</span>
                  </Button>
                )}
              </div>
            </div>

            {/* Áä∂ÊÄÅ‰ø°ÊÅØÔºàÈùûËøûÊé•Áä∂ÊÄÅÊó∂ÊòæÁ§∫Ôºâ */}
            {callStatus !== 'connected' && (
              <div className="absolute top-6 left-1/2 transform -translate-x-1/2">
                <div className="bg-black/50 backdrop-blur-sm rounded-full px-4 py-2">
                  <p className={`text-sm ${getStatusColor()}`}>{getStatusText()}</p>
                </div>
              </div>
            )}

            {/* ËøúÁ®ãÈü≥È¢ëÈùôÈü≥ÊåáÁ§∫ */}
            {callStatus === 'connected' && !remoteAudioEnabled && (
              <div className="absolute top-6 right-6">
                <div className="bg-red-500/80 backdrop-blur-sm rounded-full px-3 py-1">
                  <span className="text-white text-xs">üîá ÂØπÊñπÂ∑≤ÈùôÈü≥</span>
                </div>
              </div>
            )}

            {/* Ë∞ÉËØï‰ø°ÊÅØÔºàÂºÄÂèëÁéØÂ¢ÉÔºâ */}
            {import.meta.env.DEV && (
              <div className="absolute bottom-6 left-6 text-xs text-gray-400 bg-black/50 backdrop-blur-sm rounded px-2 py-1">
                <p>Áä∂ÊÄÅ: {callStatus}</p>
                <p>ÊúçÂä°: {isServiceInitialized ? 'Â∑≤ÂàùÂßãÂåñ' : 'Êú™ÂàùÂßãÂåñ'}</p>
                <p>Ê®°Âºè: {mode}</p>
                <p>Êú¨Âú∞ËßÜÈ¢ë: {isVideoEnabled ? 'ÂºÄÂêØ' : 'ÂÖ≥Èó≠'}</p>
                <p>ËøúÁ®ãËßÜÈ¢ë: {remoteVideoEnabled ? 'ÂºÄÂêØ' : 'ÂÖ≥Èó≠'}</p>
              </div>
            )}
          </div>
        </div>
      </DialogContent>
    </Dialog>
  );
};

export default VideoCallModal; 